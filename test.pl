use Modern::Perl;
use Test::More;
use IPC::Open3;
use Data::Dumper;

my @testcases = (
    {
        in => '01 12 31 32',
        out => [
            "12 23 31",
            "23 31 12",
            "31 12 23",
            "32 21 13",
            "21 13 32",
            "13 32 21",
        ],
    },
    {
        in => '01 12 34 30 42',
        out => [
            "21 10 03 34 42",
            "01 12 24 43 30",
            "12 24 43 30 01",
            "42 21 10 03 34",
            "34 42 21 10 03",
            "03 34 42 21 10",
            "30 01 12 24 43",
            "24 43 30 01 12",
            "10 03 34 42 21",
            "43 30 01 12 24",
        ],
    },
    {
        in => '01 12 23 34 45 55',
        out => [
            "55",
        ],
    },
    {
        in => '55 54 53 52 51 50',
        out => [
            "55",
        ],
    },
    {
        in => '35 65 55 54 51',
        out => [
            "55",
        ],
    },
    {
        in => '01 12 23 34 45 56',
        out => [
            "0",
        ],
    },
    {
        in => '01 44 20 26 64 42',
        out => [
            "26 64 44 42",
            "64 44 42 26",
            "44 42 26 64",
            "42 26 64 44",
            "62 24 44 46",
            "24 44 46 62",
            "44 46 62 24",
            "46 62 24 44",
        ],
    },
    {
        in => '35 10 54 20 12 34',
        out => [
            "35 54 43",
            "54 43 35",
            "43 35 54",
            "53 34 45",
            "34 45 53",
            "53 34 45",
