my ( $in, $out, $err );
foreach my $testcase ( @testcases ) {
    open3( $in, $out, $err, 'perl golf.pl' );
    print $in $testcase->{in};
    close $in;
    my $rout = join('',<$out>);

    chomp $rout;

    ok((grep {$rout eq $_} @{$testcase->{out}}), "input $testcase->{in}");
}

done_testing();